package conditional;

public class IfExample {

	public static void main(String[] args) {
		int x = 5;    
		int y = 12; 
		
		if(x+y > 20)
		{    
			// True Block
		System.out.println("x + y is greater than 20");  
		
		}    
		else 
		{
			// Else Block
			System.out.println("x + y is Less than 20"); 
		}
	}

}
